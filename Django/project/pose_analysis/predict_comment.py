def evaluate_workout(workout_name, predicted, workout_data):
    """
    주어진 운동 이름과 예측 배열을 바탕으로 각 조건의 평가 메시지를 반환합니다.
    
    Parameters:
      workout_name (str): 운동 이름 (예: "푸시업")
      predicted (list of int): 각 조건에 대한 예측 결과 배열 (예: [1, 0, 1, 0, 1])
      workout_data (dict): 미리 정의된 운동 데이터 딕셔너리
      
    Returns:
      list of dict: 각 조건별로 condition, value, message를 담은 리스트
    """
    if workout_name not in workout_data:
        raise ValueError(f"'{workout_name}' 운동은 등록되어 있지 않습니다.")
    
    entry = workout_data[workout_name]
    conditions = entry["conditions"]
    messages = entry["messages"]
    
    if len(predicted) != len(conditions):
        raise ValueError(f"예측 배열 길이({len(predicted)})가 조건 수({len(conditions)})와 일치하지 않습니다.")
    
    results = []
    for i, value in enumerate(predicted):
        results.append({
            "condition": conditions[i],
            "value": value,
            "message": messages[i].get(value, "정의되지 않은 값입니다.")
        })
    return results

if __name__ == "__main__":
    # 예시: "푸시업" 운동에 대해 예측 결과 [1, 1, 1, 0, 1]인 경우
    workout_name = "Push-Up"
    predicted_values = [1, 1, 1, 0, 1]
    
    evaluation = evaluate_workout(workout_name, predicted_values, workout_data)
    msg_list = []
    for eval_item in evaluation:
        print(f"Condition: {eval_item['condition']}")
        print(f"Value: {eval_item['value']}")
        msg_list.append(eval_item['message'])
        print(f"Message: {eval_item['message']}\n")
    
    print(msg_list)
